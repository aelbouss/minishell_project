#include "../minishell.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	const char	*s = (const char *)src;
	char		*d;

	d = (char *)dest;
	if (d <= s || d > s + n)
		ft_memcpy(d, s, n);
	else
	{
		while (n > 0)
		{
			d[n - 1] = s[n - 1];
			n--;
		}
	}
	return (dest);
}
